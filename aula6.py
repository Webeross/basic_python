nome = 'Eduardo'
altura = 1.95
peso = 93
imc = peso /altura ** 2
# O f deixa inserir variáveis em uma string, 2f são númros decimais na casa ex, 1f = 1,9, 2f = 1,95
linha_1 = f'{nome} tem {altura:.2f} de altura'
linha_2 = f'{peso}KG e seu imc é'
linha_3 = f'{imc:.2f}'

print(linha_1)
print(linha_2)
print(linha_3)




